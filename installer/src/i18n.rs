pub fn get() -> &'static Strings {
    if let Ok(tag) = std::env::var("WHIRLING_INSTALLER_LANG") {
        return pick(&tag);
    }
    let locale = sys_locale::get_locale().unwrap_or_default();
    pick(&locale)
}

/// Map a BCP-47-ish tag ("de", "pt-BR", "zh-Hant-TW") to a language table.
pub fn pick(tag: &str) -> &'static Strings {
    let tag = tag.trim().to_lowercase().replace('_', "-");
    // Traditional Chinese before the generic zh match.
    if tag.starts_with("zh") {
        if tag.contains("hant") || tag.contains("tw") || tag.contains("hk") || tag.contains("mo") {
            return &ZH_TW;
        }
        return &ZH;
    }
    if tag.starts_with("pt") {
        return &PT_BR;
    }
    let primary = tag.split('-').next().unwrap_or("");
    match primary {
        "ar" => &AR,
        "de" => &DE,
        "es" => &ES,
        "fr" => &FR,
        "ja" => &JA,
        "ko" => &KO,
        "pl" => &PL,
        "ru" => &RU,
        "tr" => &TR,
        _ => &EN,
    }
}

/// Fill {name}-style slots. Slots may appear in any order in a translation.
pub fn fill(template: &str, args: &[(&str, &str)]) -> String {
    let mut out = template.to_string();
    for (key, value) in args {
        out = out.replace(&format!("{{{key}}}"), value);
    }
    out
}

